# Solicitar número
numero = float(input("Ingrese un número: "))  # Usamos float para permitir números decimales

# Calcular el cuadrado y el cubo del número
cuadrado = numero ** 2
cubo = numero ** 3

# resultados
print(f"El cuadrado de {numero} es: {cuadrado}")
print(f"El cubo de {numero} es: {cubo}")
